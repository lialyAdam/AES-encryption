void AES_encrypt(unsigned char* message, unsigned char* expandedKey) {
    unsigned char state[16];
    memcpy(state, message, 16);

    const int numberOfRounds = 10; // AES-128 has 10 rounds

    // Initial round key addition
    AddRoundKey(state, expandedKey);

    for (int round = 1; round < numberOfRounds; round++) {
        // Core AES operations for each round
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(state, expandedKey + (16 * round));
    }

    // Final round without MixColumns
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(state, expandedKey + 160); // 160 = 16 * numberOfRounds

    // Copy the encrypted state back to the original message buffer
    memcpy(message, state, 16);
}


int hexToDecimal(char hex) {
    if (hex >= '0' && hex <= '9') {
        return hex - '0';
    }
    else if (hex >= 'A' && hex <= 'F') {
        return hex - 'A' + 10;
    }
    else if (hex >= 'a' && hex <= 'f') {
        return hex - 'a' + 10;
    }
    return -1; // wron hex character
}



