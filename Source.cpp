// The first round key is the key itself.
// 'memcpy' for copying arrays and passing the input key and output expanded keys as arguments.
void KeyExpansion(const unsigned char* key, unsigned char* expandedKeys) {
    // Copy the original key into the first 16 bytes of expandedKeys
    memcpy(expandedKeys, key, 16);

    unsigned char temp[4];
    int bytesGenerated = 16;
    int RconIteration = 1;

    while (bytesGenerated < 176) {
        // Copy the last 4 bytes of the currently generated key to temp
        memcpy(temp, expandedKeys + bytesGenerated - 4, 4);

        // Every 16 bytes, we need to perform special transformations
        if (bytesGenerated % 16 == 0) {
            // Rotate the 4-byte word (left circular shift)
            unsigned char k = temp[0];
            temp[0] = temp[1];
            temp[1] = temp[2];
            temp[2] = temp[3];
            temp[3] = k;

            // Apply the S-box to each byte of the word
            temp[0] = SBox[temp[0]];
            temp[1] = SBox[temp[1]];
            temp[2] = SBox[temp[2]];
            temp[3] = SBox[temp[3]];

            // XOR the first byte of temp with the round constant
            temp[0] ^= Rcon[RconIteration++];
        }

        // XOR temp with the 16th byte before the current position to generate new bytes
        expandedKeys[bytesGenerated] = expandedKeys[bytesGenerated - 16] ^ temp[0];
        expandedKeys[bytesGenerated + 1] = expandedKeys[bytesGenerated - 15] ^ temp[1];
        expandedKeys[bytesGenerated + 2] = expandedKeys[bytesGenerated - 14] ^ temp[2];
        expandedKeys[bytesGenerated + 3] = expandedKeys[bytesGenerated - 13] ^ temp[3];

        // update the number of bytes generated so far
        bytesGenerated += 4;
    }
}



